package com.example.study_with_me.model;

public class AlarmSampledata {
    private String name;
    private String time;

    public AlarmSampledata(String name, String time) {
        this.name = name;
        this.time = time;
    }
    public String getName() {
        return this.name;
    }
    public String getTime() {
        return this.time;
    }
}
